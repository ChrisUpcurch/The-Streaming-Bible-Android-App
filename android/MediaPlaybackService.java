    @Override
    public void onDestroy() {
        // Make sure our notification is gone. For compatibility with old API
        stopForegroundCompat(PLAYBACKSERVICE_STATUS);

        // Check that we're not being destroyed while something is still playing.
        if (isPlaying()) {
            Log.e(LOGTAG, "Service being destroyed while still playing.");
        }
        // release all MediaPlayer resources, including the native player and wakelocks
        mPlayer.release();
        mPlayer = null;

        //supported since API 8
        /*
        if (Integer.parseInt(Build.VERSION.SDK) >= 8)
        	mAudioManager.abandonAudioFocus(mAudioFocusListener);
		*/
        // make sure there aren't any other messages coming
        mDelayedStopHandler.removeCallbacksAndMessages(null);
        mMediaplayerHandler.removeCallbacksAndMessages(null);

        if (mCursor != null) {
            mCursor.close();
            mCursor = null;
        }

        unregisterReceiver(mIntentReceiver);
        if (mUnmountReceiver != null) {
            unregisterReceiver(mUnmountReceiver);
            mUnmountReceiver = null;
        }
        mWakeLock.release();
        
        sInstance = null;
        
        TSBApplication app = (TSBApplication)getApplication();
        app.savePositions(app.preferences);
        
        super.onDestroy();
    }
    
